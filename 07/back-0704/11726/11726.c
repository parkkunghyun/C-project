// 2 * n 크기 직사각형 채우기!
// 9개가 55가 되게 만들기!

// 1개만 있으면 -> 1가지 방법
// 2개만 있으면 -> 2가지 방법
// 3개 있으면 
// 4개 있으면 1,3 / 22 => 
// 1 2 3 5 8 13 21 34 55 

//  --| |--  |||  
 
 // ---- |||| --|| ||-- |--| 

 #include<stdio.h>

long dp[1001];
 int main(){
    dp[1] = 1;
    dp[2] = 2;

    int n;
    scanf("%d",&n);
    for(int i=3; i<=n ;i++) {
        dp[i] = (dp[i-1]+ dp[i-2]) % 10007;
    }
    
    printf("%d\n", dp[n]);
    return 0;
 }